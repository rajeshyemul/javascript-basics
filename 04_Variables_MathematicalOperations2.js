// Mathematical Operations in JavaScript
// This program demonstrates basic arithmetic operators

var num1 = 200;
let num2 = 100.1;

// Addition
// Note: "+" is also used for string concatenation
console.log("num1+num2: " + (num1 + num2));

// Subtraction
console.log("num1-num2: " + (num1 - num2));

// Multiplication
console.log("num1*num2: " + (num1 * num2));

// Division
console.log("num1/num2: " + (num1 / num2));

// Modulus (remainder)
console.log("num1%num2: " + (num1 % num2));

/*
========================
Key Learning Points
========================
1. JavaScript supports basic arithmetic operators: +, -, *, /, %.
2. The + operator is used for both addition and string concatenation.
3. Parentheses are important to ensure correct arithmetic calculation.
4. Subtraction, multiplication, division, and modulus work only on numbers.
5. Modulus (%) returns the remainder after division.
6. JavaScript uses Number type for all numeric calculations.
7. Be careful with decimal calculations due to floating-point precision.
*/
