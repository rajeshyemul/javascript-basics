// Updating and adding object properties

let project = {
    name: "Automation Framework",
    status: "In Progress"
};

// Update existing property
project.status = "Completed";

// Add a new property
project.duration = "6 Months";

console.log(project);

/*
========================
Key Learning Points
========================
1. Object properties can be updated after creation.
2. New properties can be added dynamically.
3. Objects in JavaScript are mutable.
*/
