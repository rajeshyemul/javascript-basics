// For Loop
// This program demonstrates how a `for` loop works in JavaScript

// In a `for` loop, all three parts are written in one line:
// 1. Initialization  → where the loop starts
// 2. Condition       → how long the loop should run
// 3. Increment       → how the loop moves to the next value

for (let index = 0; index < 11; index++) {

    // Print the current value of index
    // This will print numbers from 0 to 10
    console.log(index);
}

// This line executes after the loop has finished completely
console.log("Success");

/*
========================
Key Learning Points
========================
1. for loop is used when the number of iterations is known.
2. Initialization runs once at the start.
3. Condition is checked before each iteration.
4. Increment executes after every loop cycle.
5. Code outside the loop runs after the loop completes.
*/