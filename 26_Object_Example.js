// JavaScript Object - Basics
// An object stores data in key-value pairs

let person = {
    name: "Rajesh",
    role: "Test Automation Engineer",
    experience: 15
};

// Access object properties
console.log("Name: " + person.name);
console.log("Role: " + person.role);
console.log("Experience: " + person.experience);

/*
========================
Key Learning Points
========================
1. Objects store related data in key-value pairs.
2. Keys are also called properties.
3. Values can be of any data type.
4. Dot notation is commonly used to access properties.
*/
