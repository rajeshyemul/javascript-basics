// Accessing object properties

let employee = {
    name: "Anita",
    department: "QA",
    location: "Pune"
};

// Dot notation
console.log("Name: " + employee.name);

// Bracket notation
console.log("Department: " + employee["department"]);

/*
========================
Key Learning Points
========================
1. Dot notation is simple and commonly used.
2. Bracket notation is useful for dynamic property access.
3. Both methods access the same data.
*/
