// Template Literals
// Used to create strings with embedded expressions

let userName = "Rajesh";
let role = "Test Automation Engineer";
let experience = 15;

// Using template literals with backticks (` `)
let message = `Name: ${userName}, Role: ${role}, Experience: ${experience} years`;

console.log(message);

/*
========================
Key Learning Points
========================
1. Template literals use backticks (`) instead of quotes.
2. ${} is used to embed variables and expressions.
3. Makes string concatenation easier and more readable.
4. Supports multi-line strings.
5. Commonly used in logging and dynamic messages.
*/
